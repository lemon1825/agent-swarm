"""Result export — save Swarm results as markdown, HTML, or JSON.

Usage:
    from agent_swarm import Swarm, save_result

    result = await swarm.run("Analyze competitors")

    save_result(result, "report.md")     # Markdown
    save_result(result, "report.html")   # HTML with styling
    save_result(result, "report.json")   # Raw JSON
"""

__all__ = ['save_result']

import json
import os
import time
from typing import Dict


def save_result(result: Dict, path: str) -> str:
    """Save a Swarm result to file.

    Supported formats:
        .md   — Markdown report
        .html — Styled HTML report
        .json — Raw JSON data

    Args:
        result: Swarm.run() result dict
        path: Output file path (extension determines format)

    Returns:
        Absolute path to saved file
    """
    ext = os.path.splitext(path)[1].lower()
    os.makedirs(os.path.dirname(path) or ".", exist_ok=True)

    if ext == ".md":
        content = _to_markdown(result)
    elif ext == ".html":
        content = _to_html(result)
    elif ext == ".json":
        content = json.dumps(result, indent=2, ensure_ascii=False, default=str)
    else:
        content = _to_markdown(result)

    with open(path, "w", encoding="utf-8") as f:
        f.write(content)

    return os.path.abspath(path)


def _to_markdown(result: Dict) -> str:
    """Convert result to markdown report."""
    meta = result.get("metadata", {})
    goal = meta.get("goal", "Agent Swarm Report")
    lines = [
        f"# {goal}",
        "",
        f"*Generated by Agent Swarm — {time.strftime('%Y-%m-%d %H:%M')}*",
        "",
        "---",
        "",
    ]

    # Summary
    lines.append("## Summary")
    lines.append("")
    lines.append(f"- Tasks: {meta.get('succeeded', 0)}/{meta.get('total_tasks', 0)} succeeded")
    lines.append(f"- Waves: {meta.get('waves', 0)}")
    lines.append(f"- Time: {meta.get('execution_time_s', 0):.1f}s")
    tokens = meta.get("total_tokens", 0)
    if tokens:
        lines.append(f"- Tokens: {tokens:,}")
        lines.append(f"- Est. cost: ${tokens * 0.0000003:.4f}")
    lines.append("")
    lines.append("---")
    lines.append("")

    # Task results
    lines.append("## Results")
    lines.append("")

    results = result.get("results", {})
    for tid, r in results.items():
        status = "✅" if r.success else "❌"
        role = getattr(r, "role", "")
        role_str = f" ({role})" if role else ""
        lines.append(f"### {status} {tid}{role_str}")
        lines.append("")
        if r.success:
            lines.append(r.output or "(empty)")
        else:
            lines.append(f"**Error:** {r.error}")
        lines.append("")

    # Final output
    final = result.get("final_output", "")
    if final:
        lines.append("---")
        lines.append("")
        lines.append("## Final Output")
        lines.append("")
        lines.append(final)

    return "\n".join(lines)


def _to_html(result: Dict) -> str:
    """Convert result to styled HTML report."""
    md = _to_markdown(result)
    meta = result.get("metadata", {})
    goal = meta.get("goal", "Agent Swarm Report")

