def _md_to_html(md: str) -> str:
    """Minimal markdown to HTML (no external dependencies)."""
    import re

    lines = md.split("\n")
    html = []
    in_list = False
    in_code = False

    for line in lines:
        stripped = line.strip()

        # Code blocks
        if stripped.startswith("```"):
            if in_code:
                html.append("</code></pre>")
                in_code = False
            else:
                html.append("<pre><code>")
                in_code = True
            continue

        if in_code:
            html.append(_escape(line))
            continue

        # Close list if needed
        if in_list and not stripped.startswith("- "):
            html.append("</ul>")
            in_list = False

        # Headers
        if stripped.startswith("### "):
            html.append(f"<h3>{_inline(stripped[4:])}</h3>")
        elif stripped.startswith("## "):
            html.append(f"<h2>{_inline(stripped[3:])}</h2>")
        elif stripped.startswith("# "):
            html.append(f"<h1>{_inline(stripped[2:])}</h1>")
        elif stripped.startswith("---"):
            html.append("<hr>")
        elif stripped.startswith("- "):
            if not in_list:
                html.append("<ul>")
                in_list = True
            html.append(f"<li>{_inline(stripped[2:])}</li>")
        elif stripped == "":
            html.append("")
        else:
            html.append(f"<p>{_inline(stripped)}</p>")

    if in_list:
        html.append("</ul>")
    if in_code:
        html.append("</code></pre>")

    return "\n".join(html)


def _inline(text: str) -> str:
    """Process inline markdown (bold, italic, code)."""
    import re
    text = _escape(text)
    text = re.sub(r'\*\*(.+?)\*\*', r'<strong>\1</strong>', text)
    text = re.sub(r'\*(.+?)\*', r'<em>\1</em>', text)
    text = re.sub(r'`(.+?)`', r'<code>\1</code>', text)
    return text


def _escape(text: str) -> str:
    """Escape HTML entities."""
    return (text.replace("&", "&amp;").replace("<", "&lt;")
                .replace(">", "&gt;").replace('"', "&quot;"))
